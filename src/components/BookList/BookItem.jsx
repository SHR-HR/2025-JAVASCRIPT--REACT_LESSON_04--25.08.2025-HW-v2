// Импортируем CSS-модуль для стилизации компонента
import styles from './BookList.module.scss'

// Компонент BookItem для отображения отдельной книги в списке
// Принимает объект book в качестве пропса
function BookItem({ book }) {
  // Возвращаем JSX разметку для элемента списка книги
  return (
    // Элемент списка с применением стилей из CSS-модуля
    <li className={styles.bookItem}>
      
      {/* Заголовок книги (название) */}
      <h3 className={styles.bookTitle}>
        {book.title} {/* Отображаем название книги из объекта book */}
      </h3>
      
      {/* Информация о количестве страниц */}
      <p className={styles.bookPages}>
        {/* Отображаем количество страниц и добавляем "стр." (страницы) */}
        {book.pages} стр.
        
        {/* Условный рендеринг: если количество страниц больше 400, показываем бейдж */}
        {book.pages > 400 && (
          // Бейдж (метка) для больших книг
          <span className={styles.badge}>
            Большая {/* Текст бейджа */}
          </span>
        )}
      </p>
    </li>
  )
}

// Экспортируем компонент для использования в других частях приложения
export default BookItem




