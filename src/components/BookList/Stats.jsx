// Импортируем CSS-модуль для стилизации компонента
import styles from './BookList.module.scss'

// Компонент Stats для отображения статистики по книгам
// Принимает два пропса:
// - total: общее количество найденных книг
// - over400: количество книг с более чем 400 страницами
function Stats({ total, over400 }) {
  // Рендерим компонент статистики
  return (
    // Контейнер для статистики с применением стилей из CSS-модуля
    <div className={styles.stats}>
      
      {/* Параграф с общим количеством книг */}
      <p>
        Найдено книг: {/* Статический текст */}
        <strong>{total}</strong> {/* Динамическое значение общего количества книг */}
      </p>
      
      {/* Параграф с количеством книг, имеющих более 400 страниц */}
      <p>
        Из них страниц &gt; 400: {/* Статический текст с HTML-сущностью > (больше) */}
        <strong>{over400}</strong> {/* Динамическое значение количества больших книг */}
      </p>
    </div>
  )
}

// Экспортируем компонент для использования в других частях приложения
export default Stats




