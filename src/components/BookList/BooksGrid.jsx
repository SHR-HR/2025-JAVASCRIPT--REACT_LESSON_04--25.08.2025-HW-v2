// Импортируем компонент BookItem для отображения отдельной книги
import BookItem from './BookItem'
// Импортируем CSS-модуль для стилизации компонента
import styles from './BookList.module.scss'

// Компонент BooksGrid для отображения сетки книг
// Принимает массив books в качестве пропса
function BooksGrid({ books }) {
  // Возвращаем JSX разметку для сетки книг
  return (
    // Элемент неупорядоченного списка с применением стилей из CSS-модуля
    // aria-label для доступности: описывает назначение списка для скринридеров
    <ul className={styles.books} aria-label="Список отфильтрованных книг">
      
      {/* Условный рендеринг: проверяем есть ли книги в массиве */}
      {books.length > 0 ? (
        // Если книги есть: отображаем каждую книгу с помощью компонента BookItem
        books.map((book) => (
          // Для каждой книги создаем компонент BookItem
          // key={book.id} - уникальный ключ для оптимизации React
          // book={book} - передаем объект книги в качестве пропса
          <BookItem key={book.id} book={book} />
        ))
      ) : (
        // Если книг нет: отображаем сообщение о пустом списке
        <li className={styles.emptyMessage}>
          Книг не найдено {/* Текст сообщения */}
        </li>
      )}
    </ul>
  )
}

// Экспортируем компонент для использования в других частях приложения
export default BooksGrid




