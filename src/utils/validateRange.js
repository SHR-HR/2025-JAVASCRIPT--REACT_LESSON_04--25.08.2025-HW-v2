// Функция для валидации диапазона значений (минимального и максимального)
export function validateRange(minStr, maxStr) {
  // Преобразуем строковые значения в числа, если они не пустые
  const min = minStr ? Number(minStr) : null
  const max = maxStr ? Number(maxStr) : null
  
  // Проверка: если одно из значений не является валидным числом
  if ((minStr && isNaN(min)) || (maxStr && isNaN(max))) {
    return {
      isValid: false, // Диапазон невалиден
      message: 'Введите корректные числовые значения', // Сообщение об ошибке
      min: null, // Сбрасываем минимальное значение
      max: null  // Сбрасываем максимальное значение
    }
  }
  
  // Проверка: если минимальное значение больше максимального
  if (min !== null && max !== null && min > max) {
    return {
      isValid: false, // Диапазон невалиден
      message: 'Минимум больше максимума — диапазон не применяется', // Сообщение об ошибке
      min: null, // Сбрасываем минимальное значение
      max: null  // Сбрасываем максимальное значение
    }
  }
  
  // Проверка на отрицательные значения
  if ((min !== null && min < 0) || (max !== null && max < 0)) {
    return {
      isValid: false, // Диапазон невалиден
      message: 'Количество страниц не может быть отрицательным', // Сообщение об ошибке
      min: null, // Сбрасываем минимальное значение
      max: null  // Сбрасываем максимальное значение
    }
  }
  
  // Если все проверки пройдены успешно
  return {
    isValid: true, // Диапазон валиден
    message: null, // Сообщения об ошибке нет
    min, // Возвращаем числовое минимальное значение (или null)
    max  // Возвращаем числовое максимальное значение (или null)
  }
}





