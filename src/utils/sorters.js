// Утилиты для сортировки массива книг

// Функция для сортировки по возрастанию количества страниц, затем по названию
export function byPagesAscThenTitle(a, b) {
  // Сначала сравниваем по количеству страниц по возрастанию
  if (a.pages !== b.pages) {
    // Если количество страниц разное, возвращаем разницу
    // Положительное значение: a должен быть после b
    // Отрицательное значение: a должен быть перед b  
    // Ноль: порядок не меняется
    return a.pages - b.pages
  }
  
  // При равном количестве страниц - сортируем по заголовку в алфавитном порядке
  // Используем localeCompare для корректного сравнения строк с учетом локали
  return a.title.localeCompare(b.title)
}

// Функция для сортировки по убыванию количества страниц, затем по названию
export function byPagesDescThenTitle(a, b) {
  // Сначала сравниваем по количеству страниц по убыванию
  if (a.pages !== b.pages) {
    // Обратный порядок сравнения: b.pages - a.pages
    // Положительное значение: a должен быть после b (b > a)
    // Отрицательное значение: a должен быть перед b (b < a)
    // Ноль: порядок не меняется
    return b.pages - a.pages
  }
  
  // При равном количестве страниц - сортируем по заголовку в алфавитном порядке
  // Используем localeCompare для корректного сравнения строк с учетом локали
  return a.title.localeCompare(b.title)
}






